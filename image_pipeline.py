def thresholdColorGradient(img, s_thresh=(170, 255), sx_thresh=(20, 100), combined=False):
    '''
    Creates new image based on threshold values 
    Params:
    - img: Image in RGB format
    - s_thresh: min and max values for the S color channel
    - sx_thresh: min and max values for the x Sobel derivative
    - combined: if True we return a bw (1 channel) image, otherwise 3 channels
    '''
    img = np.copy(img)
    # Convert to HLS color space and separate the V channel
    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HLS).astype(np.float)
    l_channel = hsv[:,:,1]
    s_channel = hsv[:,:,2]
    # Sobel x
    sobelx = cv2.Sobel(l_channel, cv2.CV_64F, 1, 0) # Take the derivative in x
    abs_sobelx = np.absolute(sobelx) # Absolute x derivative to accentuate lines away from horizontal
    scaled_sobel = np.uint8(255*abs_sobelx/np.max(abs_sobelx))
    
    # Threshold x gradient
    sxbinary = np.zeros_like(scaled_sobel)
    sxbinary[(scaled_sobel >= sx_thresh[0]) & (scaled_sobel <= sx_thresh[1])] = 1
    
    # Threshold color channel
    s_binary = np.zeros_like(s_channel)
    s_binary[(s_channel >= s_thresh[0]) & (s_channel <= s_thresh[1])] = 1
    # Stack each channel
    # Note color_binary[:, :, 0] is all 0s, effectively an all black image. It might
    # be beneficial to replace this channel with something else.
    if combined:
        response = np.zeros_like(sxbinary)
        response[(s_binary==1) | (sxbinary==1)] = 1
    else:
        response = np.dstack(( np.zeros_like(sxbinary), sxbinary, s_binary))
    return response    


def perspectiveTransformation(img, mtx, dist,M, combined=False):
    '''
    Change prespective of image based on 
    Params:
        - img: Image in RGB to change perspective
        - mtx: 
        - dist
        - M:
        - combined: If True then we return a one single channel image (gray) otherwise several channels
    returns:
        image transformed.
    '''
    shape = (img.shape[1], img.shape[0])
    
    imgres = thresholdColorGradient(img, (150, 255), (40, 80) , combined)
    dst = cv2.undistort(imgres, mtx, dist, None, mtx)
    warped = cv2.warpPerspective(dst, M, shape, flags=cv2.INTER_LINEAR)
    return warped



def findLines(img, nwindows=9, getImage=False):
    '''
    Find the lines of a perspective transformed image
    '''
    
    histogram = np.sum(img[img.shape[0]//2:,:], axis=0)
    
    out_img = None
    
    if getImage: 
        out_img = np.dstack((img, img, img))*255
    midpoint = np.int(histogram.shape[0]/2)
    leftx_base = np.argmax(histogram[:midpoint])
    rightx_base = np.argmax(histogram[midpoint:]) + midpoint

    window_height = np.int(img.shape[0]/nwindows)
    nonzero = img.nonzero()
    nonzeroy = np.array(nonzero[0])
    nonzerox = np.array(nonzero[1])

    leftx_current = leftx_base
    rightx_current = rightx_base
    margin = 100
    minpix = 50
    left_lane_inds = []
    right_lane_inds = []

    for window in range(nwindows):
        win_y_low = img.shape[0] - (window+1)*window_height
        win_y_high = img.shape[0] - window*window_height
        win_xleft_low = leftx_current - margin
        win_xleft_high = leftx_current + margin
        win_xright_low = rightx_current - margin
        win_xright_high = rightx_current + margin
        if getImage:
            # Draw the windows on the visualization imaage
            cv2.rectangle(out_img,(win_xleft_low,win_y_low),(win_xleft_high,win_y_high),(0,255,0), 2) 
            cv2.rectangle(out_img,(win_xright_low,win_y_low),(win_xright_high,win_y_high),(0,255,0), 2) 
        # Identify the nonzero pixels in x and y within the window
        good_left_inds = ((nonzeroy >= win_y_low) & (nonzeroy < win_y_high) & (nonzerox >= win_xleft_low) & (nonzerox < win_xleft_high)).nonzero()[0]
        good_right_inds = ((nonzeroy >= win_y_low) & (nonzeroy < win_y_high) & (nonzerox >= win_xright_low) & (nonzerox < win_xright_high)).nonzero()[0]
        # Append these indices to the lists
        left_lane_inds.append(good_left_inds)
        right_lane_inds.append(good_right_inds)
        # If you found > minpix pixels, recenter next window on their mean position
        if len(good_left_inds) > minpix:
            leftx_current = np.int(np.mean(nonzerox[good_left_inds]))
        if len(good_right_inds) > minpix:        
            rightx_current = np.int(np.mean(nonzerox[good_right_inds]))
            
    # Concatenate the arrays of indices
    left_lane_inds = np.concatenate(left_lane_inds)
    right_lane_inds = np.concatenate(right_lane_inds)
    
    # Extract left and right line pixel positions
    leftx = nonzerox[left_lane_inds]
    lefty = nonzeroy[left_lane_inds] 
    rightx = nonzerox[right_lane_inds]
    righty = nonzeroy[right_lane_inds] 
    
    # Fit a second order polynomial to each
    left_fit = np.polyfit(lefty, leftx, 2)
    right_fit = np.polyfit(righty, rightx, 2)
    
    if getImage:
        out_img[nonzeroy[left_lane_inds], nonzerox[left_lane_inds]] = [255, 0, 0]
        out_img[nonzeroy[right_lane_inds], nonzerox[right_lane_inds]] = [0, 0, 255]
        #return left_fit, right_fit, left_lane_inds, right_lane_inds, out_img
        return left_fit, right_fit, out_img
    else:
        #return left_fit, right_fit, left_lane_inds, right_lane_inds
        return left_fit, right_fit


